	當然，如果是你出現在綿羊牆上可能就不那麼有趣了，但這只是善意的提醒。:D
 
  8/30 ~ 31 DSConf  http://twconf.data-sci.org/
	國內首次舉辦的資料科學研討會，探討資料科學與資料分析相關領域，
	資料分析是近年來大資料(Big data) 時代的顯學。
 
  10/11 ~ 12 JSDC 2014  http://2014.jsdc.tw/
	國內網頁前端技術首屈一指的研討會，因為橫跨設計、後端、前端三個領域，一向以充滿活力與內容多元著稱。

  10/25 ~ 26 MOPCON 2014  (Mobile Open Platform Conference) http://mopcon.org/2014/
  南台灣總是給人熱情艷陽天的印象，在南台灣確實也有一場極為熱血的開發者聚會在進行，
  特別標榜「堅持在濁水溪以南舉辦」的MOPCON，不選擇在資源豐沛的北台灣，
  而是在高雄市舉辦一連2天的研討會，無論是參與者人數或是講師的水準都是一等一，
  或者該說熱度更強，因為南台灣這樣大型的活動，又是免費入場，畢竟不常見。

  讓我們一起體驗濁水溪以南的風光，體驗南部人的科技熱情！


  11/8  g0v summit (g0v 高峰會) https://g0v.hackpad.com/g0v-summit-2014--3Ulg4cd3T9I 
