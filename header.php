  接下來我想向您介紹開源社群的現況與盛事：
 

 
  台灣開放原始碼運動與相關技術社群已經蓬勃近十年，
 
  從早期少數同好交流，到如今每週都有許多工程師在台北各咖啡廳聚會，
 
  這些工程師們熱血的傳遞訊息與交流技術，在這台灣小小地島上孕育出許多人才，
  有時甚至還能引領業界的技術風向與作為業界獵才的目標。
 
 
  資訊業，又稱是軟體業。
 
  我們常笑說我們有這麼一種軟實力，因為台灣的人才常常被挖到國外，
  我們都能自豪的說，我們是來自於台灣的軟體人，這是能力的象徵。
 
  但這我認為這樣的環境與能力，
  首要歸功於由開發者組成的軟體社群，所帶來的聚會與分享風氣！
 
 
  這群人付出自己每週、每月的一部分時間分享所知、所得，
  引導更多人進入資深的領域，帶領更多人瞭解技術的有趣之處與容易偏惡之處。
 
 
  相信能看到這一段話的你肯定是軟體業的開發者，
  於是我希望能向你介紹幾個接下來開源與技術社群很重要的幾個社群年會：
 
  7/19~20 COSCUP 2014 大會  http://coscup.org/2014/zh-tw/
 
  	國內最大的開放原始碼活動，
	近幾界來都吸引上千位開源運動開發者，成員往往耗時數月籌備，內容精彩豐富。
 
	但是也有一貫的門票不收費，以向開發者與廠商募資贊助的傳統。
 
	在台灣的軟體業中，幾乎可以說是開源界人才的培育處與伸展台。

 
  8/19 ~ 22 HITCON 2014   http://hitcon.org/2014/
 
	非常神秘的駭客年會。雖說我們常誤認為駭客與黑客，認為駭客是一群只會運用技術的人，
	但其實駭客界一直不遺餘力的參與與提昇國內的資訊安全品質與宣導。
 
	駭客年會往往也都為我們帶來許多最新的資安攻防手法討論。
 
	目前駭客年會團隊夥伴也正組隊進軍全球駭客競賽，挑戰全球二十強。
	http://www.ithome.com.tw/news/88072
 
